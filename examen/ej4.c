#include <unistd.h>

int	main(void)
{
	int i;
	char ascii;

	i = 0;
	while (i >= 0 && i <= 9)
	{
		if (i % 2 != 0)
		{
			ascii = i + '0';
			write (1, &ascii, 1);
		}
		i++;
	}
	return (0);
}
